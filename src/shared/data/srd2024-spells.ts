/** Filtres pour la recherche de sorts */
export function getSpellsByClass(className: string): SpellData[] {
  return SRD2024_SPELLS.filter(spell => spell.classes.includes(className.toLowerCase()))
}

export function getSpellsByLevel(level: number): SpellData[] {
  return SRD2024_SPELLS.filter(spell => spell.level === level)
}

export function getSpellsBySchool(school: SpellData['school']): SpellData[] {
  return SRD2024_SPELLS.filter(spell => spell.school === school)
}

export function getRitualSpells(): SpellData[] {
  return SRD2024_SPELLS.filter(spell => spell.ritual)
}

export function searchSpells(query: string): SpellData[] {
  const lowerQuery = query.toLowerCase()
  return SRD2024_SPELLS.filter(spell => 
    spell.name.toLowerCase().includes(lowerQuery) ||
    spell.description.toLowerCase().includes(lowerQuery) ||
    spell.school.toLowerCase().includes(lowerQuery)
  )
}

export function getSpellById(id: string): SpellData | undefined {
  return SRD2024_SPELLS.find(spell => spell.id === id)
}

/** Convertit un SpellData en PreparedSpell pour le personnage */
export function spellDataToPreparedSpell(spell: SpellData): PreparedSpell {
  return {
    name: spell.name,
    level: spell.level,
    casting_time: spell.castingTime,
    components: spell.components,
    concentration: spell.concentration,
    range: spell.range,
    description: spell.description,
  }
}